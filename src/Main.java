    private void showLanguageSelection() {
    JFrame languageFrame = new JFrame("Select Language / Seleccionar Idioma");
    languageFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    languageFrame.setSize(300, 150);
    languageFrame.setLocationRelativeTo(this);
    
    // Cambiar a BorderLayout para mejor organizaci칩n
    languageFrame.setLayout(new BorderLayout());
    
    // Panel para los botones con FlowLayout centrado
    JPanel buttonPanel = new JPanel(new FlowLayout(FlowLayout.CENTER, 10, 20));
    
    JButton spanishButton = new JButton("Espa침ol");
    JButton englishButton = new JButton("English");

    spanishButton.addActionListener(e -> {
        languageFrame.dispose();
        MedicalDecisionTreeGUI tree = new MedicalDecisionTreeGUI("ES_es");
        tree.iniciarDiagnostico();
    });

    englishButton.addActionListener(e -> {
        languageFrame.dispose();
        MedicalDecisionTreeGUI tree = new MedicalDecisionTreeGUI("EN_us");
        tree.iniciarDiagnostico();
    });
    
    buttonPanel.add(spanishButton);
    buttonPanel.add(englishButton);
    
    // Agregar el panel de botones al centro del BorderLayout
    languageFrame.add(buttonPanel, BorderLayout.CENTER);
    
    // Opcional: Agregar un peque침o margen en la parte inferior
    languageFrame.add(new JPanel(), BorderLayout.SOUTH);
    
    languageFrame.setVisible(true);
}

